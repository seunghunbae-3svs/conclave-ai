export interface WriteWorkflowInput {
  cwd: string;
  /**
   * Force overwrite even when the existing file has been
   * user-customised (no managed-header marker). Reserved for the
   * `conclave init --reconfigure` escape hatch.
   */
  force?: boolean;
}

/**
 * v0.13.18 — write outcome.
 *
 *   "created"   — file did not exist; wrote fresh.
 *   "migrated"  — file existed AND carried our managed-header marker
 *                 AND its content differed from the current template
 *                 → overwrote (we own this file). Live RC: PR #34
 *                 manually patched eventbadge's old-shape conclave-
 *                 merge.yml because `conclave init` couldn't tell
 *                 "this is a stale-template file we generated" from
 *                 "this is something the user wrote". Now it can.
 *   "skipped-current" — file matches the current template byte-for-byte;
 *                       no work needed.
 *   "skipped-customised" — file exists, lacks the managed-header
 *                          marker; treat as user-owned, leave alone.
 *                          --force overrides.
 */
export type WriteWorkflowStatus =
  | "created"
  | "migrated"
  | "skipped-current"
  | "skipped-customised";

export interface WriteWorkflowOutput {
  path: string;
  status: WriteWorkflowStatus;
  /** Compatibility with pre-v0.13.18 callers — true when the file was
   * NOT touched (skipped-current OR skipped-customised). */
  skipped: boolean;
  /** Compatibility — true when the file was newly created (NOT migrated). */
  created: boolean;
}

/**
 * Header marker we put on every workflow file we own. If a file
 * starts with this string we treat it as "ours to update"; if not,
 * we treat it as user-customised and leave alone (unless --force).
 *
 * Both content templates start with `# Generated by \`conclave init\`.`
 * so any of them is detectable by this single substring check.
 */
const MANAGED_HEADER_MARKER = "Generated by `conclave init`";

/**
 * Render a one-line status message for an init step. Centralised so
 * `conclave init` and any future consumer (test snapshots, dry-run
 * preview) format these consistently.
 */
export function formatWorkflowStatusLine(
  relativePath: string,
  status: WriteWorkflowStatus,
): string {
  switch (status) {
    case "created":
      return `• wrote:    ${relativePath} (new)\n`;
    case "migrated":
      return `• migrated: ${relativePath} (was an older managed template — auto-updated)\n`;
    case "skipped-current":
      return `• ok:       ${relativePath} (already up to date)\n`;
    case "skipped-customised":
      return `• keep:     ${relativePath} (user-customised — preserved; pass --reconfigure to overwrite)\n`;
  }
}

async function pathExists(p: string): Promise<boolean> {
  return fs
    .access(p)
    .then(() => true)
    .catch(() => false);
}

function isManagedFile(content: string): boolean {
  // Only check the first 200 chars — the marker is on line 1 of all
  // our templates and we don't want a deep scan.
  return content.slice(0, 200).includes(MANAGED_HEADER_MARKER);
}

/**
 * Single workhorse for write-or-migrate semantics. All three
 * `writeXxxWorkflow` functions delegate here so the migration logic
 * lives in exactly one place.
 */
async function writeOrMigrate(
  target: string,
  template: string,
  force: boolean,
): Promise<WriteWorkflowOutput> {
  const exists = await pathExists(target);
  if (!exists) {
    await fs.mkdir(path.dirname(target), { recursive: true });
    await fs.writeFile(target, template, "utf8");
    return { path: target, status: "created", skipped: false, created: true };
  }
  // File exists. Decide between skip-current / migrate / skip-customised / force-overwrite.
  let existing = "";
  try {
    existing = await fs.readFile(target, "utf8");
  } catch {
    // Read failure on an existing file is rare. Fall through to
    // overwrite under --force; otherwise treat as customised.
  }
  if (force) {
    await fs.writeFile(target, template, "utf8");
    // --force on an unchanged file still counts as "migrated" so the
    // user gets a clear "I overwrote it" log. Same effective result.
    return { path: target, status: "migrated", skipped: false, created: false };
  }
  if (existing === template) {
    return { path: target, status: "skipped-current", skipped: true, created: false };
  }
  if (isManagedFile(existing)) {
    // We own this file (we put the marker there). Old template →
    // migrate. This is the "auto-migration" path that closes H1 #1.
    await fs.writeFile(target, template, "utf8");
    return { path: target, status: "migrated", skipped: false, created: false };
  }
  // No marker → user-customised. Don't touch without --force.
  return { path: target, status: "skipped-customised", skipped: true, created: false };
}

export async function writeWorkflow(input: WriteWorkflowInput): Promise<WriteWorkflowOutput> {
  return writeOrMigrate(
    path.join(input.cwd, WORKFLOW_PATH),
    WORKFLOW_CONTENT,
    !!input.force,
  );
}

/**
 * v0.10 — write the rework dispatcher workflow alongside the main
 * conclave.yml. Idempotent: matches → skip; managed + stale →
 * auto-migrate; user-customised → preserve unless --force.
 */
export async function writeReworkWorkflow(
  input: WriteWorkflowInput,
): Promise<WriteWorkflowOutput> {
  return writeOrMigrate(
    path.join(input.cwd, REWORK_WORKFLOW_PATH),
    REWORK_WORKFLOW_CONTENT,
    !!input.force,
  );
}

/**
 * v0.13.17 — write the merge dispatcher workflow. Same migration
 * semantics as writeReworkWorkflow.
 */
export async function writeMergeWorkflow(
  input: WriteWorkflowInput,
): Promise<WriteWorkflowOutput> {
  return writeOrMigrate(
    path.join(input.cwd, MERGE_WORKFLOW_PATH),
    MERGE_WORKFLOW_CONTENT,
    !!input.force,
  );
}
