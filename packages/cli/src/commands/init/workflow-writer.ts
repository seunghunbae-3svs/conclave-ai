/**
 * The 3-line (effective) wrapper workflow per D7. Users DO NOT edit this
 * file — everything that matters (action versions, review logic, memory
 * persistence, telegram posting) lives in the reusable workflow we host.
 * `secrets: inherit` passes the user's own LLM keys through without
 * surfacing them. The header comment tells humans reading their own repo
 * exactly what's happening and where to look.
 */
export const WORKFLOW_CONTENT = `# Generated by \`conclave init\`. Do not edit — conclave updates via its reusable workflow.
# Source: https://github.com/${REUSABLE_REF.split("@")[0]!.replace(".github/workflows/review.yml", "")}
name: Conclave AI

on:
  pull_request:
    types: [opened, synchronize, reopened]

jobs:
  conclave:
    uses: ${REUSABLE_REF}
    permissions:
      contents: read
      pull-requests: write
    secrets: inherit
`;

/**
 * v0.10 — consumer-side dispatcher for the autonomous rework loop.
 *
 * Listens for `repository_dispatch` of type `conclave-rework`, fired by
 * the central plane (apps/central-plane) whenever a review verdict
 * comes back as "rework" AND `cycle < max_cycles`. The dispatch payload
 * carries `pr_number`, `cycle`, and `max_cycles`; this file forwards
 * them into our reusable rework.yml which runs `conclave autofix`,
 * commits + pushes, and re-triggers review via the resulting
 * `pull_request: synchronize` event.
 *
 * Without this file the central plane fires the dispatch event but the
 * consumer repo has no listener — the loop never closes. Pre-v0.10
 * installs that miss this file will only get review (no autonomous
 * rework); they should re-run `conclave init --reconfigure` to pick it
 * up.
 */
export const REWORK_WORKFLOW_CONTENT = `# Generated by \`conclave init\`. Do not edit — conclave updates via its reusable workflow.
# v0.10 — consumer-side dispatcher for the autonomous rework loop.
# Source: https://github.com/${REWORK_REUSABLE_REF.split("@")[0]!.replace(".github/workflows/rework.yml", "")}
name: Conclave AI Rework

on:
  repository_dispatch:
    types: [conclave-rework]

jobs:
  rework:
    uses: ${REWORK_REUSABLE_REF}
    permissions:
      contents: write
      pull-requests: write
    secrets: inherit
    with:
      pr-number: \${{ github.event.client_payload.pr_number }}
      cycle: \${{ github.event.client_payload.cycle }}
      max-cycles: \${{ github.event.client_payload.max_cycles }}
`;

